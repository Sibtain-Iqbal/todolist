import React, { useState } from 'react';
import { questions } from './questions';

function Faqs() {
  const [activeFaqId, setActiveFaqId] = useState(null);

  const toggleFaq = (id) => {
    // Toggle the FAQ: if it's already active, deactivate it, otherwise activate the selected FAQ
    setActiveFaqId(activeFaqId === id ? null : id);
  };

  return (
    <div>
      <h1>Frequently Asked Questions (FAQs)</h1>
      <div className='faqContainer'>
        {questions.map(question => (
          <FaqItem
            key={question.id}
            question={question}
            isActive={activeFaqId === question.id}
            onToggle={() => toggleFaq(question.id)}
          />
        ))}
      </div>
    </div>
  );
}

function FaqItem({ question, isActive, onToggle }) {
  return (
    <div className='faqItem'>
      <h2 onClick={onToggle}>{question.question}</h2>
      <p className={isActive ? 'activeFaq' : 'inactiveFaq'}>{question.answer}</p>
    </div>
  );
}

export default Faqs;




const Apssp = () => {
  const [currencies, setCurrencies] = useState([]);
  const [fromCurrency, setFromCurrency] = useState('USD');
  const [toCurrency, setToCurrency] = useState('EUR');
  const [amount, setAmount] = useState(1);
  const [exchangeRate, setExchangeRate] = useState(1);
  const [result, setResult] = useState(null);




  const convertCurrency = () => {
    setResult((amount * exchangeRate).toFixed(2));
  };

  return (
    <div style={{ padding: '20px', textAlign: 'center' }}>
      <h1>Currency Converter</h1>
      <div style={{ marginBottom: '10px' }}>
        <input
          type="number"
          value={amount}
          onChange={(e) => setAmount(e.target.value)}
          style={{ padding: '5px', marginRight: '10px' }}
        />
       
        <span>to</span>
        <select
          value={toCurrency}
          onChange={(e) => setToCurrency(e.target.value)}
          style={{ padding: '5px', marginLeft: '10px' }}
        >
          {currencies.map((currency) => (
            <option key={currency} value={currency}>
              {currency}
            </option>
          ))}
        </select>
      </div>
      <button onClick={convertCurrency} style={{ padding: '10px 20px' }}>
        Convert
      </button>
      {result && (
        <div style={{ marginTop: '20px', fontSize: '20px' }}>
          <p>
            {amount} {fromCurrency} = {result} {toCurrency}
          </p>
        </div>
      )}
    </div>
  );
};

